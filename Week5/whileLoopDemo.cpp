#include <iostream>
using namespace std;

int main(){
    int number = 0; //int number to zero

    //while the number is less than 5, do the following
    while (number < 5){
        //print hello
        cout <<"Hello\n";
        //increment number by 1
        number++;
    }

    cout <<"That's all!\n"; //print thats all
    return 0;// end
}